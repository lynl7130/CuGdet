from model.BaseEntity import BaseEntity


class Honors(BaseEntity):
    table = "honors"

    def __init__(self, data):
        self.hid = data['hid']
        self.name = data['name']
        self.type = data['type']
        self.tag = data['tag']
        self.amt = data['amt']
        self.starting = data['starting']
        self.ending = data['ending']
